function out = sigmav(T)

a_1 = 2.855e-18; %m^3 s^-1 keV^(2/3)
a_2 = 19.98; %keV^(1/3)
T   = T*1e-3; %temperature in keV
c_k = [1 0.330891 -3.08039 11.0695 -19.6836 20.1261 -12.4204 4.77148 -1.15068  0.169684 -0.014005 0.000496334];
S = 0;
for j=1:length(c_k)
    S = S + (1 + 5/12* T.^(1/3)/a_2)*c_k(j).*T.^((j-1)/3);
end
out = a_1./T.^(2/3) .*S .*exp(-a_2./T.^(1/3));
    
end